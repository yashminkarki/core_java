package method.demo;

public class MethodDemo {
	
	static void sum()
	{
		System.out.println("Hey. I am sum method");
		System.out.println("*****************");
	}
	static void mul()
	{
		System.out.println("Hey. I am multiplication method");
		System.out.println("*****************");
	}

	public static void main(String[] args) {
		sum();
		mul();

	}

}
