package selection.statement;

public class SwitchCase {

	public static void main(String[] args) {
		String str = "chd";
		switch(str) 
		{
		default:
			System.out.println("default case is ready");
			break;
		case "BB":
			System.out.println("Case BB is ready");
			break;
		case "AA":
			System.out.println("Case AA is ready");
			break;
		case "chd":
			System.out.println("Case chd is ready");
			break;
		case "ydfhfhh":
			System.out.println("Case ydfhfhh is ready");
			break;
		}

	}

}
