//Write a Java program to demonstrate primitive datatypes with examples.
package assignment.datatype;

public class PrimitiveDataType {

	public static void main(String[] args) {
		int a = 90;
		long l = 74683;
		byte b = 4;
		short s = 567;
		char ch = 'c';
		boolean bool = true;
		
		float fl = 90.8f;
		double dbl = 90.8;
		System.out.println("Byte value is: " + b);	
		System.out.println("Short value is: " + s);
		System.out.println("Integer value is: " + a);
		System.out.println("Long value is: " + l);
		System.out.println("Character value is: " + ch);
		System.out.println("Boolean value is: " + bool);
		System.out.println("Float value is: " + fl);
		System.out.println("Double value is: " + dbl);
	}

}
