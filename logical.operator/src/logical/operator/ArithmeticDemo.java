package logical.operator;

public class ArithmeticDemo {

	public static void main(String[] args) {
		//declaring the variables
		int a = 12, b = 5;
		
		//addition operator
		System.out.println("a+b = " + (a+b));
		
		//subtraction operator
		System.out.println("a-b = " + (a-b));
		//Division operator
		System.out.println("a/b = " + (a/b));
		//Multiplication operator
		System.out.println("a*b = " + (a*b));
		//modulo operator
		System.out.println("a%b = " + (a%b));
		
		

	}

}
